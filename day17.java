public class day17{
 
  public static void main(String[]args){


    //belajar operator penugasan
 
      int a = 20;
      System.out.println(a+=10); //artinya sama dengan 20+10
 
      int b = 10;
      System.out.println(b-=9); //artinya sama dengan 10-9
 
      int c = 5;
      System.out.println(c*=8); //artinya perkalian 5*8
 
      float d = 10;
      System.out.println(d/=7); //artinya pembagian 10/7
 
      int e = 10;
      System.out.println(e%=8); //artinya sisa bagi 10 % 6 
 
      }
  }