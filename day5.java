public class day5 {
    public static void main(String[] args) {

        int angka1 = 3;
        int angka2 = 4;
        

        //pembagian
        int hasiBagi = angka1 % angka2;
        System.out.println("hasil pembagian: " +hasiBagi);

        //perkalian
        int hasilKali = angka1 * angka2;
        System.out.println("hasil perkalian: "+hasilKali);

        //penjumlahan
        int hasilPenjumlahan = angka1 + angka2;
        System.out.println("hasil penjumlahan: "+hasilPenjumlahan);

        //pengurangan
        int hasilPengurangan = angka1 - angka2;
        System.out.println("hasil pengurangan: "+hasilPengurangan);

        //sisa bagi /
        int sisaBagi = angka1/angka2;
        System.out.println("sisa bagi: "+sisaBagi);
     }

    }

     